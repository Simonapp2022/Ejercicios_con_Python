#Programa import math


import math #Importamos el módulo math

x = int(input("Ingresa un numero \n"))
raiz = math.sqrt(x) #Utilizamos la función sqrt del módulo math
print("La raíz cuadrada del número ", x, "es: ", raiz)

resultadoFactorial=math.factorial(x)
print("El factorial del número ", x, "es: ", resultadoFactorial)


